/* 2. Leer dos valores enteros e informar la suma y su cociente */

package ejerciciosLoo;

public class Ejercicio2Loo {
	
	public static void main(String[] args) {

        int a = 21;
        int b = 3;

        int suma = a + b;
        double cociente = (double) a / b;

        System.out.println("La suma de los dos numeros es: " + suma);
        System.out.println("El cociente del primer y el segundo numero es: " + cociente);

    }

}
